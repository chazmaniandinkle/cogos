func TestWriteExtractBlocks_Basic(t *testing.T) {
	dir := t.TempDir()
	blocks := []ExtractBlock{{
		OutputPath: "out.go",
		Code:       "func Hello() {}",
		SourceFile: "src.cog.md",
	}}

	paths, err := WriteExtractBlocks(blocks, dir, false)
	if err != nil {
		t.Fatal(err)
	}
	if len(paths) != 1 {
		t.Fatalf("expected 1 path, got %d", len(paths))
	}

	data, err := os.ReadFile(filepath.Join(dir, "out.go"))
	if err != nil {
		t.Fatal(err)
	}
	content := string(data)

	// Should contain generation header
	if !strings.Contains(content, "Code generated by cog extract from src.cog.md; DO NOT EDIT.") {
		t.Errorf("missing generation header in output:\n%s", content)
	}
	// Should contain the code
	if !strings.Contains(content, "func Hello() {}") {
		t.Errorf("missing code in output:\n%s", content)
	}
}

func TestWriteExtractBlocks_WithPackage(t *testing.T) {
	dir := t.TempDir()
	blocks := []ExtractBlock{{
		OutputPath: "out.go",
		Package:    "main",
		Code:       "func Hello() {}",
		SourceFile: "src.cog.md",
	}}

	_, err := WriteExtractBlocks(blocks, dir, false)
	if err != nil {
		t.Fatal(err)
	}

	data, err := os.ReadFile(filepath.Join(dir, "out.go"))
	if err != nil {
		t.Fatal(err)
	}
	content := string(data)

	if !strings.Contains(content, "package main") {
		t.Errorf("missing package declaration in output:\n%s", content)
	}
	// package declaration should come after the header and before the code
	headerIdx := strings.Index(content, "DO NOT EDIT.")
	pkgIdx := strings.Index(content, "package main")
	codeIdx := strings.Index(content, "func Hello() {}")
	if headerIdx >= pkgIdx || pkgIdx >= codeIdx {
		t.Errorf("unexpected ordering: header@%d, package@%d, code@%d", headerIdx, pkgIdx, codeIdx)
	}
}

func TestWriteExtractBlocks_DryRun(t *testing.T) {
	dir := t.TempDir()
	blocks := []ExtractBlock{{
		OutputPath: "should-not-exist.go",
		Code:       "func Nope() {}",
		SourceFile: "src.cog.md",
	}}

	paths, err := WriteExtractBlocks(blocks, dir, true)
	if err != nil {
		t.Fatal(err)
	}
	if len(paths) != 1 {
		t.Fatalf("expected 1 path returned, got %d", len(paths))
	}

	// File should NOT exist on disk
	outPath := filepath.Join(dir, "should-not-exist.go")
	if _, err := os.Stat(outPath); err == nil {
		t.Errorf("file %s should not exist in dry-run mode", outPath)
	}
}

func TestWriteExtractBlocks_Append(t *testing.T) {
	dir := t.TempDir()

	// First write (non-append) to create the file
	first := []ExtractBlock{{
		OutputPath: "combined.go",
		Code:       "func First() {}",
		SourceFile: "src.cog.md",
	}}
	_, err := WriteExtractBlocks(first, dir, false)
	if err != nil {
		t.Fatal(err)
	}

	// Second write (append) to add to the file
	second := []ExtractBlock{{
		OutputPath: "combined.go",
		Append:     true,
		Code:       "func Second() {}",
		SourceFile: "src.cog.md",
	}}
	_, err = WriteExtractBlocks(second, dir, false)
	if err != nil {
		t.Fatal(err)
	}

	data, err := os.ReadFile(filepath.Join(dir, "combined.go"))
	if err != nil {
		t.Fatal(err)
	}
	content := string(data)

	if !strings.Contains(content, "func First() {}") {
		t.Errorf("missing first function in output:\n%s", content)
	}
	if !strings.Contains(content, "func Second() {}") {
		t.Errorf("missing second function in output:\n%s", content)
	}
	// The append block should NOT add a second generation header
	count := strings.Count(content, "DO NOT EDIT.")
	if count != 1 {
		t.Errorf("expected exactly 1 generation header, found %d", count)
	}
}
