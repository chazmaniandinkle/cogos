func TestParseExtractBlocks_SingleBlock(t *testing.T) {
	content := []byte("# Doc\n\n```go\n// extract: cmd/main.go\nfmt.Println(\"hello\")\n```\n")

	blocks, err := ParseExtractBlocks(content, "doc.cog.md")
	if err != nil {
		t.Fatal(err)
	}
	if len(blocks) != 1 {
		t.Fatalf("expected 1 block, got %d", len(blocks))
	}

	b := blocks[0]
	if b.OutputPath != "cmd/main.go" {
		t.Errorf("OutputPath = %q, want %q", b.OutputPath, "cmd/main.go")
	}
	if b.Code != `fmt.Println("hello")` {
		t.Errorf("Code = %q, want %q", b.Code, `fmt.Println("hello")`)
	}
	if b.SourceFile != "doc.cog.md" {
		t.Errorf("SourceFile = %q, want %q", b.SourceFile, "doc.cog.md")
	}
	// The opening fence is on line 3 (1-indexed)
	if b.LineNumber != 3 {
		t.Errorf("LineNumber = %d, want %d", b.LineNumber, 3)
	}
}

func TestParseExtractBlocks_MultipleBlocks(t *testing.T) {
	content := []byte(strings.Join([]string{
		"# Doc",
		"",
		"```go",
		"// extract: a.go",
		"func A() {}",
		"```",
		"",
		"Some text",
		"",
		"```go",
		"// extract: b.go",
		"func B() {}",
		"```",
	}, "\n"))

	blocks, err := ParseExtractBlocks(content, "multi.cog.md")
	if err != nil {
		t.Fatal(err)
	}
	if len(blocks) != 2 {
		t.Fatalf("expected 2 blocks, got %d", len(blocks))
	}
	if blocks[0].OutputPath != "a.go" {
		t.Errorf("blocks[0].OutputPath = %q, want %q", blocks[0].OutputPath, "a.go")
	}
	if blocks[1].OutputPath != "b.go" {
		t.Errorf("blocks[1].OutputPath = %q, want %q", blocks[1].OutputPath, "b.go")
	}
	if blocks[0].Code != "func A() {}" {
		t.Errorf("blocks[0].Code = %q, want %q", blocks[0].Code, "func A() {}")
	}
	if blocks[1].Code != "func B() {}" {
		t.Errorf("blocks[1].Code = %q, want %q", blocks[1].Code, "func B() {}")
	}
}

